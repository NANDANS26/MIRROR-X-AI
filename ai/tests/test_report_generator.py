# 4. ReportGenerationError raised on invalid/empty session data
# ===========================================================================

class TestReportGenerationError:
    """
    Req 8.1 – generate_report SHALL raise ReportGenerationError on failure.
    """

    def test_error_class_exists(self):
        assert ReportGenerationError is not None

    def test_error_is_exception_subclass(self):
        assert issubclass(ReportGenerationError, Exception)

    def test_error_has_message_attribute(self):
        err = ReportGenerationError("test message")
        assert err.message == "test message"

    def test_error_has_cause_attribute(self):
        cause = ValueError("root cause")
        err = ReportGenerationError("wrapper", cause=cause)
        assert err.cause is cause

    def test_raises_on_none_input(self):
        """Passing None instead of a dict must raise ReportGenerationError."""
        with pytest.raises(ReportGenerationError):
            generate_report(None)  # type: ignore[arg-type]

    def test_raises_on_non_dict_input(self):
        """Passing a non-dict must raise ReportGenerationError."""
        with pytest.raises(ReportGenerationError):
            generate_report("not a dict")  # type: ignore[arg-type]

    def test_raises_on_integer_input(self):
        with pytest.raises(ReportGenerationError):
            generate_report(42)  # type: ignore[arg-type]

    def test_empty_dict_does_not_raise(self):
        """
        An empty dict is valid session data (all fields optional / with defaults).
        The generator must produce a PDF without raising.
        """
        result = generate_report({})
        assert isinstance(result, bytes)
        assert result[:4] == b"%PDF"

    def test_minimal_session_no_patterns_succeeds(self):
        """Session with no detected_patterns should generate a valid PDF."""
        session = {
            "session_id": "empty-patterns",
            "source_url": "https://clean.example.com",
            "detected_patterns": [],
            "scores": {
                "manipulation_score": {"score": 0.0, "contributions": []},
                "trust_score": {"score": 100.0, "contributions": []},
                "friction_score": {"score": 0.0, "contributions": []},
                "ux_fairness_index": "Fair",
            },
            "simulation_results": [],
        }
        result = generate_report(session)
        assert result[:4] == b"%PDF"


