def _generate(session: dict | None = None) -> bytes:
    """Call generate_report with SAMPLE_SESSION (or a custom dict) and return bytes."""
    return generate_report(session if session is not None else SAMPLE_SESSION)


# ===========================================================================
# 1. PDF bytes validity
# ===========================================================================

class TestPDFBytesValidity:
    """
    Req 8.1 – Report_Generator SHALL produce a PDF.
    The result must be non-empty raw bytes beginning with the %PDF magic bytes.
    """

    def test_returns_bytes_type(self):
        result = _generate()
        assert isinstance(result, bytes), "generate_report must return bytes"

    def test_bytes_are_non_empty(self):
        result = _generate()
        assert len(result) > 0, "PDF bytes must not be empty"

    def test_starts_with_pdf_magic_bytes(self):
        result = _generate()
        assert result[:4] == b"%PDF", (
            f"PDF must start with %PDF magic bytes, got {result[:4]!r}"
        )

    def test_pdf_version_header_present(self):
        """The %PDF magic bytes are followed by a version number like %PDF-1.x."""
        result = _generate()
        header = result[:8].decode("latin-1", errors="replace")
        assert header.startswith("%PDF-"), (
            f"Expected %PDF-<version> header, got {header!r}"
        )

    def test_minimum_reasonable_size(self):
        """A valid multi-section PDF should be at least a few kilobytes."""
        result = _generate()
        assert len(result) >= 4096, (
            f"PDF is suspiciously small ({len(result)} bytes); expected >= 4096"
        )


